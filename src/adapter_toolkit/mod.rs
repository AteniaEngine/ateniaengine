//! **Adapter Toolkit v2** тАФ a declarative layer *on top of* the v1
//! model-adapter system (`crate::model_adapters`).
//!
//! The toolkit lets a model be described by a small YAML/JSON
//! document instead of a hand-written Rust adapter. It is a strict
//! addition: it never modifies v1, never touches the runtime core
//! or the graph builders, and never generates Rust code. A v2
//! adapter is a v1 adapter *parameterised by data* тАФ see
//! [`generator::GeneratedAdapter`].
//!
//! Pipeline:
//!
//! ```text
//!   .yaml/.json  тФАтФАdslтФАтФАтЦ╢  AdapterDsl
//!   AdapterDsl   тФАтФАspecтФАтЦ╢  ResolvedAdapterSpec   (Part 3 IR + Part 5 catalog)
//!   spec         тФАтФАgenтФАтФАтЦ╢  GeneratedAdapter      (impl AteniaModelAdapter)
//!   adapters     тФАтФАregтФАтФАтЦ╢  AdapterRegistry       (v2-first, v1-fallback)
//! ```
//!
//! **Parts 2тАУ5** (DSL, generator, registry, pattern normalisation)
//! live here today. Parts 6тАУ9 (validation, introspection, CLI,
//! auto-detection) extend the same module.

